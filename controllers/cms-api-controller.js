    let cospace = req.body;
    
    httpHelper.postRequest("coSpaces/"+cospace.cospaceId+"/coSpaceUsers","userId="+cospace.userId+"&userJid="+cospace.userJid+"&autoGenerated="+cospace.autoGenerated)
